    <h1>Prabowo Sambangi Presiden Vietnam H.E. Tô Lâm</h1>
    <p>Kompas.com - 14/09/2024, 14:18 WIB</p>
    <h3>JAKARTA, KOMPAS.com - Menteri Pertahanan RI sekaligus Presiden Terpilih RI 2024-2029, Prabowo Subianto bertemu dengan Presiden Vietnam H.E. Tô Lâm pada Jumat (13/9/2024) di Vietnam, Hanoi. Kunjungan itu dilakukan Prabowo usai berkegiatan di Ibu Kota Nusantara (IKN), Kalimantan Timur. Dilansir dari siaran pers yang diterima Kompas.com, Prabowo tiba di Noi Bai International Airport, Hanoi pukul 16.15 waktu setempat dan langsung disambut hangat oleh Wakil Menteri Luar Negeri Vietnam, H.E. Do Hung Viet. Selain itu, hadir pula Dubes Vietnam Untuk RI, H.E. Ta Van Thong, Director-General, South East Asia, South Asia, South Pacific Department Mr. Nguyen T?t Thành dan Athan RI di Vietnam Kolonel Laut (P) Dian Tri Hutanto. Selanjutnya, Prabowo bersama Presiden Vietnam makan malam bersama dan dilanjutkan dengan pertemuan bilateral antar kedua negara.</h3>
